def _build_preflight_evidence_records(implementation: dict[str, Any]) -> list[dict[str, Any]]:
    records: list[dict[str, Any]] = []

    for item in implementation["check_implementations"]:
        check_name = item["check_name"]
        records.append(
            {
                "preflight_evidence_record_id": f"preflight-evidence-record-{check_name}",
                "check_name": check_name,
                "evidence_type": item["evidence_type"],
                "evidence_record_status": "record_model_defined_not_generated",
                "evidence_record_required": True,
                "evidence_record_generated": False,
                "check_implemented": False,
                "check_satisfied": False,
                "input_sources": list(item["input_sources"]),
                "observed_inputs": [],
                "validation_result": "not_evaluated",
                "failure_mode": "not_evaluated",
                "fail_closed": True,
                "mismatch_detected": False,
                "stale_state_detected": False,
                "unsafe_state_detected": False,
                "ai_visible": False,
                "raw_artifact_capture_permitted": False,
                "sanitized_summary_required": True,
                "responsibility": item["responsibility"],
                "record_requirements": [
                    "record must be generated by a future concrete preflight check implementation",
                    "record must remain non-AI-visible until sanitized if it contains raw inputs or raw outputs",
                    "record must preserve enough detail to reconstruct the preflight decision",
                    "record must fail closed when required inputs are missing",
                    "record must fail closed when runtime, target, scope, or authorization binding mismatches are detected",
                    "record must fail closed when stale state is detected",
                    "record must not authorize execution by itself",
                ],
            }
        )

    return records


